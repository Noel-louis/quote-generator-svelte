<h2>Présentation du projet</h2>

<p>
  Ce projet est un générateur de citations aléatoires. Il permet de générer des
  images à partir d'une citation aléatoire et d'une image aléatoire, avec la
  possibilité de changer la taille de l'image et de télécharger l'image générée.
</p>
<p>
  L'application a été développé dans le cadre d'un projet universitaire pour le
  module de développement svelte.
</p>

