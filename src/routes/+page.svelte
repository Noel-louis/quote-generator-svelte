<h1>Bienvenue sur le site de génération de citations aléatoires</h1>

<p>
  Ce site vous permet de générer des citations aléatoires et de les afficher sur
  une image aléatoire. et de pouvoir les télécharger pour les partager avec vos
  amis.
</p>
<p>
  Les citations sont générées à partir de l'API Quotable, qui fournit des
  citations en anglais de différents auteurs.
</p>
<p>Voici quelques examples :</p>
<ul>
  <li>
    <img src="/image(1).png" alt="Image 1" />
  </li>
  <li>
    <img src="/image(2).png" alt="Image 2" />
  </li>
  <li>
    <img src="/image(3).png" alt="Image 3" />
  </li>
</ul>

